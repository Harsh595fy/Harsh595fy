def log_text():
    logs = []  # List to store logs in memory
    print("Enter text (type 'exit' to stop):")
    while True:
        user_input = input("> ")
        if user_input.lower() == "exit":
            print("Logging stopped.")
            break
        logs.append(user_input)
        print(f"Logged: {user_input}")
    
    print("\nAll Logged Entries:")
    for log in logs:
        print(log)

log_text()
